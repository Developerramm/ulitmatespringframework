package OOPs;

public class Addition {

    public int add(int a, int b) {
        return a + b;
    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }

    public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }

    public double add(double i, double j) {
        return i + j;
    }

    public String add(String s1, String s2) {
        return s1 + s2;
    }

}
